<p align="justify">
    Los síntomas son <strong>fatiga</strong>, <strong>falta de apetito</strong>
, <strong>dolor de estómago</strong>, <strong>náuseas</strong> e    <strong>ictericia</strong>, raras veces produce la muerte.
</p>
<br>
<p align="justify">
    Los anticuerpos producidos por VHA duran toda la vida y protegen contra la
    reinfección.
</p>
<br>
<p align="justify">
    La trasmisión principal es fecal-oral, cuando una persona no infectada
    ingiere alimentos o agua contaminados por las heces de un individuo
    infectado, puede ocurrir a través de la suciedad de las manos de la persona
    encargada de cocinar. La transmisión hídrica no es frecuente; cuando
    ocurre, suele estar relacionada con la contaminación por aguas residuales o
    el abastecimiento de agua insuficientemente tratada.
</p>
<br>
<p align="justify">
    El contagio también se puede producir por contacto físico estrecho con una
    persona infectada —por ejemplo, por mediante el sexo bucoanal.
</p>
