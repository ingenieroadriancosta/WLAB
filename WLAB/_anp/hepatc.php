<p align="justify">
    <strong>¿Cuál es la diferencia entre hepatitis A, hepatitis B y hepatitis C?</strong>
    <br>
    <br>
    La hepatitis A, la hepatitis B y la hepatitis C son infecciones hepáticas 
    causadas por tres virus diferentes. Aunque cada uno puede causar síntomas 
    similares, tienen diferentes modos de transmisión y pueden afectar el 
    hígado de manera diferente. 
    <br> 
    <br>
    La hepatitis A generalmente es una infección a 
    corto plazo y no se vuelve crónica. 
    <br> 
    <br>
    La hepatitis B y la hepatitis C también pueden comenzar como infecciones 
    agudas a corto plazo, pero en algunas personas, el virus permanece en el cuerpo, 
    lo que resulta en enfermedades crónicas y problemas hepáticos a largo plazo.
    <br> 
    <br>
    Existen vacunas para prevenir la hepatitis A y la hepatitis B; 
    sin embargo, no hay vacuna para la hepatitis C.

