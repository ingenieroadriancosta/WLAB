<p align="justify">
    La hepatitis C es una enfermedad del hígado causada por el virus VHC; ese
    virus puede causar hepatitis, tanto aguda como crónica, varía entre una
    dolencia leve que dura algunas semanas, y una enfermedad grave de por vida.
</p>
<br>
<p align="justify">
    La hepatitis C es una importante causa de cáncer hepático.
</p>
<br>
<p align="justify">
    El período de incubación de la hepatitis C puede variar de 2 semanas a 6
    meses. Tras la infección inicial, aproximadamente un 80% de los casos son
    asintomáticos. Aquellos con sintomatología aguda pueden presentar fiebre,
    cansancio, inapetencia, náuseas, vómitos, dolor abdominal, orinas oscuras,
    heces claras, dolores articulares e ictericia (coloración amarillenta de la
    piel y los ojos).
</p>
<br>
<p align="justify">
    El virus de la hepatitis C se transmite por la sangre o por:
</p>
<ul type="disc">
    <li>
        por consumo de drogas inyectables y por compartir el material de
        inyección.
    </li>
    <br>
    <li>
