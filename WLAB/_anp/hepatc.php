        todo jeringas y agujas, en entornos sanitarios.
    </li>
    <br>
    <li>
        por transfusiones de sangre y productos sanguíneos sin analizar.
    </li>
    <br>
    <li>
        por prácticas sexuales que conllevan exposición a la sangre (por
        ejemplo, entre hombres con relaciones homosexuales, en especial los que
