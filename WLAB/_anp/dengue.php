<p align="justify">
    Causado por el virus del dengue (DEN), comprende cuatro serotipos distintos
(<strong>DEN-1</strong>, <strong>DEN-2</strong>, <strong>DEN-3</strong> y    <strong>DEN-4</strong>) que pertenecen al género Flavivirus, familia
    Flaviviridae.
</p>
<br>
<p align="justify">
    El mosquito <strong>Aedes aegypti</strong> es el vector principal que
    transmite los virus que causan el dengue. 
    <br>
    <br>
    Los virus se transmiten a los
humanos a través de las picaduras de un    <strong>mosquito Aedes hembra infeccioso</strong>, que adquiere
    principalmente el virus mientras se alimenta de la sangre de una persona
    infectada.
</p>
<p align="center">
    <img
        src="images/_anp/Aedes_aegypti.jpg"
        width="748"
        height="493"
        border="0"
    />
</p>
<p align="center">
    <strong>Mosquitos trasmisores del dengue. Fuente:</strong>
    <a href="ab%20https:/www.cdc.gov/chi">cdc.gov</a>
</p>
<p align="justify">
    Una vez infectados, los humanos se convierten en los principales portadores
    y multiplicadores del virus, sirviendo como fuente del virus para los
    mosquitos no infectados. El virus circula en la sangre de una persona
    infectada durante 2-7 días, aproximadamente al mismo tiempo que la persona
    desarrolla fiebre.
    <strong>
        Los pacientes que ya están infectados con el virus del dengue pueden
        transmitir la infección a través de los mosquitos
    </strong>
    Aedes después de que aparezcan los primeros síntomas (durante 4-5 días;
    máximo 12).
</p>
<p align="justify">
    <strong>
        En humanos, la recuperación de la infección por un virus del dengue
        proporciona inmunidad de por vida
    </strong>
    contra ese serotipo de virus en particular. Sin embargo, esta inmunidad
    solo confiere protección parcial y transitoria contra la infección
    posterior por los otros tres serotipos del virus. La evidencia apunta al
    hecho de que la infección secuencial aumenta el riesgo de desarrollar
    dengue grave. El intervalo de tiempo entre infecciones y la secuencia viral
    particular de infecciones también puede ser importante.
</p>


