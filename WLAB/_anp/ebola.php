<p align="justify">
    Es una de las enfermedades más mortales, la enfermedad por el virus del
    Ébola de la familia de los filovirus, antes llamada fiebre hemorrágica del
    Ébola, es una enfermedad grave, a menudo fatal, con una tasa de letalidad
    de hasta <strong>90%</strong>.
    <br>
    <br>
    Sus síntomas son: fiebre, debilidad intensa y dolor de músculos, 
    cabeza y garganta, lo cual va seguido de vómitos, diarrea, 
    erupciones cutáneas, disfunción renal y hepática y, 
    en algunos casos, hemorragias internas y externas.
</p>
<p align="justify">
    Fue identificado en 1976 en África, no se conoce el origen del virus, pero
    pruebas actuales indican que murciélagos frugívoros pueden ser huéspedes.
</p>
<p >
    El contagio sucede por el contacto con animales infectados (generalmente
    después de faenados, o durante la cocción o ingestión) o a través del
    contacto con las secreciones corporales de seres humanos infectados.
</p>
<p align="justify">
    La mayoría de los casos son provocados por la transmisión de una persona a
    otra, que se produce cuando la sangre u otros fluidos o secreciones
    corporales (heces, orina, saliva, semen) de personas infectadas ingresan en
    el cuerpo de una persona sana a través de soluciones de continuidad de la
    piel o de membranas mucosas.
</p>
