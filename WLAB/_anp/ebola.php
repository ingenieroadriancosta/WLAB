<p align="justify">
La enfermedad por el virus del Ebola (EVE) es un enfermedad grave, 
a menudo mortal en el ser humano. El virus se detectó por vez primera en 1976 
en dos brotes simultáneos ocurridos en Nzara (hoy Sudán del Sur) y Yambuku 
(República Democrática del Congo). La aldea en que se produjo el segundo de 
ellos está situada cerca del río Ebola, que da nombre al virus.

El brote de ebola de 2014-2016 en África Occidental fue el más extenso y 
complejo desde que se descubrió el virus en 1976. Hubo más casos y más muertes 
en este brote que en todos los demás juntos. Además, se extendió a diferentes 
países: empezó en Guinea y después se propagó a través de las fronteras 
terrestres a Sierra Leona y Liberia.
</p>
<br>
<br>
<p align="justify">
El género Ebolavirus es, junto con los géneros Marburgvirus y Cuevavirus, 
uno de los tres miembros de la familia Filoviridae (filovirus). 
<br>
Se han identificado cinco especies distintas en el género Ebolavirus:



<ul>
    <li>
        ebolavirus Bundibugyo (BDBV).
    </li>
    <li>
        ebolavirus Zaire (EBOV).
    </li>
    <li>
        ebolavirus Reston (RESTV).
    </li>
    <li>
        ebolavirus Sudan (SUDV).
    </li>
    <li>
        ebolavirus Taï Forest (TAFV).
    </li>
</ul>

    
    
    
    
    
