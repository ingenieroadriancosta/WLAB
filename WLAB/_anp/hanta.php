<p align="justify">
     Los Hantavirus son grupos pertenecientes a la familia Bunyaviridae 
     transmitidos por roedores.
     <br>
     <br>
     Pueden causar diversos síndromes de enfermedades en personas de todo el mundo.
     <br>
     <br>
     Los hantavirus en las Américas pueden causar el síndrome pulmonar por hantavirus (SPH).
     <br>
     <br>
     Otros hantavirus se encuentran principalmente en Europa y Asia y pueden 
     causar fiebre hemorrágica con síndrome renal (HFRS).
     <br>
     <br>
     Cada serotipo de hantavirus tiene una especie de huésped de roedor 
     específico y se transmite a las personas a través del virus en aerosol 
     que se elimina en la orina, las heces y la saliva, y con menos frecuencia 
     por una picadura de un huésped infectado. 
     <br>
     El hantavirus más importante en los Estados Unidos que puede causar HPS es 
     el virus Sin Nombre identificado en 1993, transmitido por el ratón venado.
     <br>
     <br>
     El Síndrome cardiopulmonar por hantavirus, puede ser causado por una serie 
     de virus como: Virus Sin-Nombre, Andes virus, 
     Laguna negra, Choclo, Juquitibia virus, entre otros.
