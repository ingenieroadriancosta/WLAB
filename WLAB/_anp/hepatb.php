<p align="justify">
    La hepatitis B es una infección vírica del hígado causada por el virus de
    la hepatitis B (<strong>VHB</strong>).
</p>
<br>
<p align="justify">
    Los modos de transmisión más habituales son el contagio del feto por virus
    de la madre durante el parto, a través de pinchazos, tatuajes,
    perforaciones y exposición a sangre o líquidos corporales infectados como
    la saliva, el semen y el flujo vaginal y menstrual.
    <br>
    <br>
    <strong>Cómo NO se transmite la hepatitis B</strong>
    <br>
    El virus de la hepatitis B <strong>NO</strong> se transmite al compartir 
    utensilios para comer, amamantar, abrazar, besar, tomarse de las manos, 
    toser, estornudar o por el uso recreativo de piscinas públicas o similares.
</p>
<br>
<p align="justify">
    La mayor parte de las afectados no experimentan síntomas tras infectarse,
    algunos presentan un cuadro agudo con síntomas que duran varias semanas,
    como ictericia, orina oscura, cansancio extremo, náuseas, vómitos y dolor
    abdominal. En un pequeño grupo de personas, la hepatitis aguda puede dar
    lugar a una insuficiencia hepática aguda potencialmente mortal.
</p>
<br>
<p align="justify">
    En algunos casos la hepatitis B puede causar también una infección hepática
    crónica que, posteriormente, puede ocasionar cirrosis (un endurecimiento
    del hígado) o cáncer hepático.
</p>
<br>
<p align="justify">
    No hay ninguna terapia específica para la hepatitis B <em>aguda</em>. Por
    tanto, el tratamiento persigue el bienestar y el equilibrio nutricional del
    paciente, incluida la rehidratación tras los vómitos y diarreas. Lo más
    importante es evitar medicamentos innecesarios. <strong>NO</strong> se
deben administrar <strong>antieméticos</strong> ni    <strong>paracetamol</strong>.
</p>
<br>
<p align="justify">
    De acuerdo con los cálculos de la OMS, unos 257 millones de personas
    padecían infección crónica por el virus de la hepatitis B (VHB) en 2015. Se
    considera que la infección es crónica si se da positivo en las pruebas de
    detección del antígeno de superficie del virus.
</p>
<br>
<p align="justify">
    Ese mismo año, la hepatitis B causó unas 887 000 defunciones,
    principalmente por cirrosis o carcinoma hepatocelular (es decir, cáncer
    primario del hígado).
</p>
<br>
<p align="justify">
    En 2017 contrajeron la infección 1,1 millones de personas.
</p>
<br>
<p align="justify">
    En 2016, 27 millones de personas (el 10,5% de la población total infectada
    por el VHB) sabía que tenía esta infección, mientras que 4,5 millones (el
    16,7%) de los diagnosticados estaban en tratamiento.
</p>
