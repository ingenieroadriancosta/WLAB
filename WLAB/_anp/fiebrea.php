<p align="center">
    <strong>Mosquito trasmisor de la fiebre amarilla. Fuente:</strong>
    <a href="wikipedia.org">wikipedia.org</a>
</p>
<p align="justify">
    El periodo de incubación es de 3 a 6 días. Muchos casos son asintomáticos,
    pero cuando hay síntomas, los más frecuentes son fiebre, dolores
    musculares, sobre todo de espalda, cefaleas, pérdida de apetito y náuseas o
    vómitos. En la mayoría de los casos los síntomas desaparecen en 3 o 4 días.
</p>
<p align="justify">
    Sin embargo, un pequeño porcentaje de pacientes entran a las 24 horas de la
    remisión inicial en una segunda fase, más tóxica. Vuelve la fiebre elevada
    y se ven afectados varios órganos, generalmente el hígado y los riñones. En
    esta fase son frecuentes la ictericia (color amarillento de la piel y los
    ojos, hecho que ha dado nombre a la enfermedad), el color oscuro de la
    orina y el dolor abdominal con vómitos. Puede haber hemorragias orales,
    nasales, oculares o gástricas. La mitad de los pacientes que entran en la
    fase tóxica mueren en un plazo de 7 a 10 días.
</p>

