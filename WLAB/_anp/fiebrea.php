<p align="justify">
    La fiebre amarilla es una enfermedad vírica aguda, hemorrágica, transmitida
    por mosquitos infectados. El término ”<strong>amarilla</strong>” alude a la
    ictericia que presentan algunos pacientes.
</p>
<p align="justify">
    Los síntomas de la fiebre amarilla son: fiebre, cefaleas, ictericia,
    dolores musculares, náuseas, vómitos y cansancio.
</p>
<p align="justify">
    Las epidemias se producen cuando un individuo infectado llega a un área con
    alta densidad de mosquitos y los individuos de la población no tienen
    inmunidad.
</p>
