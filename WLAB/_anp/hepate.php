<p align="justify">
    La hepatitis E es una enfermedad hepática causada por el virus de la 
    hepatitis E (VHE), de cual existen, al menos, cuatro genotipos: 
    el 1 y el 2 solo se han encontrado en el ser humano, 
    mientras que el 3 y el 4 circulan en varios animales 
    (entre ellos los cerdos, los jabalíes y los ciervos) sin causarles 
    enfermedad, e infectan ocasionalmente al ser humano.
</p>
<br>
<p align="justify">
    La hepatitis E afecta a todas las zonas del mundo y sigue dos patrones, pues puede encontrarse en:
    <li>
        Zonas con escasos recursos y contaminación frecuente del agua.
    </li>
    <br>
    <li>
        La ingestión de carne o productos cárnicos poco cocinados derivados de animales infectados (como el hígado de cerdo).
    </li>
    <br>
    <li>
        La transfusión de hemoderivados infectados.
    </li>
    <br>
    <li>
        La transmisión maternofetal.
    </li>
    <br>
   

</p>
