<p align="justify">
    El sarampión es una enfermedad muy contagiosa y grave causada por un virus 
    de la familia paramyxoviridae del género Morbillivirus.
    El virus tiene un diámetro entre 120 y 140 nanómetros.
</p>
<br>
<p align="justify">
    Antes de que la vacuna se introdujera en 1963 y se generalizara su uso,
    cada 2-3 años se registraban importantes epidemias de sarampión que
    llegaban a causar cerca de dos millones de muertes al año. En 2017 murieron
    aproximadamente 110000 personas por esta causa, la mayoría de ellas menores
    de 5 años y a pesar de existir vacunas seguras y eficaces.
</p>
<br>
<p align="justify">
    El primer signo del sarampión suele ser la fiebre alta, en la fase inicial,
    el paciente puede presentar rinorrea, tos, ojos llorosos y rojos, y
    pequeñas manchas blancas en la cara interna de las mejillas. Al cabo de
    unos tres días aparece un exantema(sarpullido), generalmente en el rostro 
    y la parte superior del cuello, que va extendiendo, acabando por afectar a 
    las manos y pies.
</p>
<br>
<p align="justify">
    El intervalo entre la exposición al virus y la aparición del exantema
    oscila entre 7 y 18 días (media de 14 días).
</p>
<br>
<p align="justify">
    La mayoría de las muertes se deben a complicaciones del sarampión, que son
    más frecuentes en menores de 5 años y adultos de más de 30 años. Las más
    graves son la ceguera, la encefalitis (infección acompañada de edema
    cerebral), la diarrea grave (que puede provocar deshidratación), las
    infecciones del oído y las infecciones respiratorias graves, como la
    neumonía. Los casos graves son especialmente frecuentes en niños pequeños
    malnutridos, y sobre todo en los que no reciben aportes suficientes de
    vitamina A o cuyo sistema inmunitario se encuentra debilitado por el
    VIH/SIDA u otras enfermedades.
</p>
<br>
<p align="justify">
    El virus del sarampión es muy contagioso y se propaga por la tos y los
    estornudos, el contacto personal íntimo o el contacto directo con
    secreciones nasales o faríngeas infectadas.
</p>
<br>
<p align="justify">
    El virus presente en el aire o sobre superficies infectadas sigue siendo
    activo y contagioso durante periodos de hasta 2 horas, y puede ser
    transmitido por un individuo infectado desde 4 días antes hasta 4 días
    después de la aparición del exantema.
</p>
